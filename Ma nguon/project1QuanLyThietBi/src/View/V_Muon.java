    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        
        if(jRadioButton1.isSelected()) itemSelected=data.timItemtheoMaTaiSan(jTextField1.getText());
        if(jRadioButton2.isSelected()) itemSelected=data.timItemtheoMaTaiSan(jTextField2.getText());
        String stringChoMuon;
        if(itemSelected!=null){
            if(itemSelected.kiemTraDaMuon()==false){
            jTextField3.setEnabled(true);
            jTextField4.setEnabled(true);
            jTextField5.setEnabled(true);
            jTextField6.setEnabled(true);
            stringChoMuon=new String("thiết bị này có thể cho mượn");
            jButton1.setEnabled(true);
            jButton2.setEnabled(true);
            }else{
                jTextField3.setEnabled(false);
                jTextField4.setEnabled(false);
                jTextField5.setEnabled(false);
                jTextField6.setEnabled(false);
                stringChoMuon=new String("thiết bị này đã có người mượn");
            }
            jRadioButton1.setEnabled(false);jTextField1.setEnabled(false);
            jRadioButton2.setEnabled(false);jTextField2.setEnabled(false);
            String chiteu = new String("<html><div>");
            jLabel2.setText(chiteu.concat(data.timLoaiTBtheoItem(itemSelected).chiTieuKyThuat).concat(" <br /> ")
                    .concat("Mã tài sản: ").concat(itemSelected.maTaiSan).concat(" <br /> ")
                    .concat("Số hiệu máy: ").concat(itemSelected.soHieuMay).concat(" <br /> ")
                    .concat(stringChoMuon)
                    .concat("</div></html>"));
        }else{
            jLabel2.setText("không tìm ra thiết bị nào");
        }
    }//GEN-LAST:event_jButton3MouseClicked

