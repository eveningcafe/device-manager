    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(jTextField4.getText()==null||jTextField4.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Không được để trống tên người mượn",
                  "Title", JOptionPane.WARNING_MESSAGE);
        }
        else{
            itemSelected.muonTra.tenNguoiMuon=jTextField4.getText();
            itemSelected.muonTra.ngayMuon=jTextField3.getText();
            itemSelected.muonTra.ngayTra=jTextField5.getText();
            itemSelected.muonTra.chuThich=jTextField6.getText();
            //use quanlythietbi; insert into MuonTra(MaTaiSan,TenNguoiMuon,NgayMuon,NgayTra,ChuThich) values ('IT.IS.9999','HOA','6/4/2017','3/1/2018','ABC')
            String q = new String("use quanlythietbi; insert into MuonTra(MaTaiSan,TenNguoiMuon,NgayMuon,NgayTra,ChuThich) values ('");
            String querry=q.concat(itemSelected.maTaiSan).concat("','").concat(jTextField4.getText()).concat("','")
                            .concat(jTextField3.getText()).concat("','").concat(jTextField5.getText()).concat("','").concat(jTextField6.getText()).concat("')");
            
            data.addquerry(querry);
        }
    }//GEN-LAST:event_jButton1MouseClicked

