    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(jRadioButton1.isSelected()){
            itemSelected=data.timItemtheoMaTaiSan(jTextField1.getText());
        }else if(jRadioButton2.isSelected()){
            itemSelected=data.timItemtheoSoHieuMay(jTextField2.getText());
        }
        if(itemSelected!=null&&itemSelected.muonTra.tenNguoiMuon!=null){
            String chiteu = new String("<html><div>");
            jLabel1.setText(chiteu.concat(data.timLoaiTBtheoItem(itemSelected).chiTieuKyThuat).concat(" <br /> ")
                    .concat("Mã tài sản: ").concat(itemSelected.maTaiSan).concat(" <br /> ")
                    .concat("Số hiệu máy: ").concat(itemSelected.soHieuMay).concat(" <br /> ")
                    .concat("</div></html>"));
            jTextField3.setEnabled(true);
            jTextField4.setEnabled(true);
            jTextField5.setEnabled(true);
            jTextField7.setEnabled(true);
            jLabel6.setText(null);
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
            jTextField3.setText(itemSelected.muonTra.tenNguoiMuon);
            jTextField4.setText(itemSelected.muonTra.ngayMuon);
            jTextField5.setText(itemSelected.muonTra.ngayTra);
            jTextField7.setText(itemSelected.muonTra.chuThich);
            NnguoiMuon=itemSelected.muonTra.tenNguoiMuon;
            NngayTra=itemSelected.muonTra.ngayTra;
            NngayMuon=itemSelected.muonTra.ngayMuon;
            NchuThich=itemSelected.muonTra.chuThich;
        }else if(itemSelected!=null&&itemSelected.muonTra.tenNguoiMuon==null){
            String chiteu = new String("<html><div>");
            jLabel1.setText(chiteu.concat(data.timLoaiTBtheoItem(itemSelected).chiTieuKyThuat).concat(" <br /> ")
                    .concat("Mã tài sản: ").concat(itemSelected.maTaiSan).concat(" <br /> ")
                    .concat("Số hiệu máy: ").concat(itemSelected.soHieuMay).concat(" <br /> ")
                    .concat("</div></html>"));
            jLabel6.setText("Thiết bị này chưa có ai mượn.");
            jTextField3.setText(null);
            jTextField4.setText(null);
            jTextField5.setText(null);
            jTextField7.setText(null);
            jTextField3.setEnabled(false);
            jTextField4.setEnabled(false);
            jTextField5.setEnabled(false);
            jTextField7.setEnabled(false);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
        }else{
            jLabel1.setText("<html><div>Không tìm thấy thiết bị nào</div></html>");
            jLabel6.setText(null);
            jTextField3.setEnabled(false);
            jTextField4.setEnabled(false);
            jTextField5.setEnabled(false);
            jTextField7.setEnabled(false);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
        }
    }//GEN-LAST:event_jButton1MouseClicked

