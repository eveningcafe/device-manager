    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        if(jTextField3.getText()==null||jTextField3.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Không được để trống tên người mượn",
                  "Lỗi", JOptionPane.WARNING_MESSAGE);
        }
        else{
            itemSelected.muonTra.tenNguoiMuon=jTextField3.getText();
            itemSelected.muonTra.ngayMuon=jTextField4.getText();
            itemSelected.muonTra.ngayTra=jTextField5.getText();
            itemSelected.muonTra.chuThich=jTextField7.getText();
            //use quanlythietbi; UPDATE MuonTra SET TenNguoiMuon='CO',NgayMuon='6/4/2017',NgayTra='11/9/2019',ChuThich='ABC' WHERE MaTaiSan='IT.IS.00002'
            String q = new String("use quanlythietbi; UPDATE MuonTra SET TenNguoiMuon='");
            String querry=q.concat(jTextField3.getText()).concat("',NgayMuon='").concat(jTextField4.getText()).concat("',NgayTra='").concat(jTextField5.getText())
                            .concat("',ChuThich='").concat(jTextField7.getText()).concat("' WHERE MaTaiSan='").concat(itemSelected.maTaiSan).concat("'");
            
            data.addquerry(querry);
        }
    }//GEN-LAST:event_jButton2MouseClicked

